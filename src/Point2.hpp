#ifndef POINT_INCLUDE
#define POINT_INCLUDE

///////////////////////////////////////////
// Headers
///////////////////////////////////////////
#include <algorithm>

constexpr double EPS{ 1E-9 }; // accuracy in calculations

///////////////////////////////////////////
/// \brief Class for manipulating 2D point
///
///////////////////////////////////////////
class Point2 final
{
public:
	///////////////////////////////////////////
	/// \brief Default constructor
	///
	/// Creates a Point(0, 0).
	/// 
	///////////////////////////////////////////
	Point2();

	///////////////////////////////////////////
	/// \brief Constructor
	///
	/// \param x X coordinate
	/// \param y Y coordinate
	///
	///////////////////////////////////////////
	Point2(double x, double y);
	
	///////////////////////////////////////////
	bool operator<(const Point2& right) const;

	///////////////////////////////////////////
	// Member data
	///////////////////////////////////////////
	double x; //!< X coordinate of the point
	double y; //!< Y coordinate of the point
};

#endif // POINT_INCLUDE